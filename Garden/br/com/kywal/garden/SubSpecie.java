package br.com.kywal.garden;

public class SubSpecie {
	private String name;
	private String code;

	public void setName(String name) {
		this.name = name;
	}	

	public String getName() {
		return this.name;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getCode() {
		return this.code;
	}
}
