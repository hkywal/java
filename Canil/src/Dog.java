
public class Dog {
	private String breed;
	private double weight;
	
	public void setBreed(String breed) {
		this.breed = breed;
	}
	
	public void getBreed() {
		System.out.println(this.breed);
	}
	
	public void setWeight(double weight) {
		this.weight = weight;
	}
	
	public double getWeight() {
		return this.weight;
	}
	
}
 